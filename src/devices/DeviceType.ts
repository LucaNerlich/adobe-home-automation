/**
 * This enumeration, DeviceType, represents distinct types of devices within the application context.
 * It defines two types of devices:
 * 1. PROVIDER: A type indicating a device that is a provider of resources or services.
 * 2. CONSUMER: A type indicating a device that is a consumer or user of resources or services.
 */
export enum DeviceType {
    PROVIDER = 'PROVIDER',
    CONSUMER = 'CONSUMER'
}
